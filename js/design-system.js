const bankingDroopdownMenu = document.querySelector(".mobile-banking-list");
const aboutDropdown = document.querySelector(".mobile-about-list");
const investDropdown = document.querySelector(".mobile-invest-list");
const mediaDropdown = document.querySelector(".mobile-media-list");
const csrDropdown = document.querySelector(".mobile-csr-list");
const openIcon = document.querySelector(".open-icon");
const mobileDropdown = document.querySelector(".mobile-dropdown");

//eeee

showBankingMenuBtn.addEventListener("click", () => {
  if (bankingDroopdownMenu.classList.contains("hide-banking-dropdown")) {
    showBankingMenuBtn.innerHTML = `<i class="ri-subtract-fill"></i>`;
    bankingDroopdownMenu.classList.remove("hide-banking-dropdown");
  } else {
    showBankingMenuBtn.innerHTML = `<i class="ri-add-fill"></i>`;
    bankingDroopdownMenu.classList.add("hide-banking-dropdown");
  }
});
